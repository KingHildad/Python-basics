# Arithmetic Operaters
x = 9
y = 3
print(x+y) #Addittion
print(x-y) #Subtraction
print(x*y) #Multication
print(x/y) #Division
print(x%y) #Modulus - returns the remainder
print(x//y) #Squareroot
print(x**y) #Squared

#Comparison Operaters - compare values
if x > y :
    print(x > y) #True
    print(y > x) #False

print(x < y)
print(y < x)
print(x == y) #Equal to
print(x != y) #Not equal to

#Assignment operaters - used to assign values to variables.
a=200
b=20
print(a+b)

a += 30 #230
print(a)
a -= 30 #170
print(a)

#Logic operaters and, or not
h=100

print(h < 250 and h > 1000)
print(h < 250 or h > 1000)
print(not(h < 250 and h > 1000))

#operator precedence-order in which operations get carried out
output = (100-4*3/1)
print(int(output))

